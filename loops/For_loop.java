package java_codes.loops;

public class For_loop {

	public static void main(String[] args) {
		//loops: one thing doing for specific condition or time 
		//1) FOR LOOP
		   // FOR(initialization;condition;opern/updation)
		   // -initialization
		    //-condition
		   // -operation on variables
		    //#ICBO 
		// iterations in for loop: it knows the range.
		//using loops again and again operations are possible 
		    for (int i=10;i<20;i++)
		    {
		    	System.out.println("hii"); 
		    	
		    }
		    for (int i=5;i>=1;i--)
		    {
		    	System.out.println("bye");
		    }
		    for (int i=5;i>=1;--i)
		    	
		    {
		    	
		    	System.out.println("anand");
		    }
	}

}
